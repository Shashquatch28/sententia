def _load_json(path: Path) -> dict:
    with open(path, encoding="utf-8") as f:
        return json.load(f)


def _load_top_matches() -> list:
    matches = []
    for path in MATCH_DIR.glob("*.json"):
        try:
            matches.append(_load_json(path))
        except Exception as exc:
            logger.warning(f"Could not load {path}: {exc}")
    matches.sort(key=lambda m: m.get("overall_match_score", 0), reverse=True)
    top = matches[:TOP_N]
    logger.info(f"Loaded {len(top)} match scores (pool: {len(matches)})")
    return top


