def _apply_decision_tree(match: dict, profile: dict) -> str:
    score = match.get("overall_match_score", 0)
    skills_matched = match.get("required_skills_matched", [])
    gaps = match.get("identified_gaps", [])
    red_flags = profile.get("red_flags", [])

    disc = match.get("discriminator_scores", {})
    disc_values = list(disc.values())
    technical_score = next(
        (v for k, v in disc.items() if "llm" in k.lower() or "tech" in k.lower()),
        disc_values[0] if disc_values else 0,
    )
    product_score = next(
        (v for k, v in disc.items() if "product" in k.lower()),
        disc_values[1] if len(disc_values) > 1 else 0,
    )

    # 1. Blocking risks
    blocking_keywords = [
        "no product experience", "pure engineer", "no leadership",
        "fraud", "fabricated", "inconsistent dates",
    ]
    has_blocking = any(
        any(kw in flag.lower() for kw in blocking_keywords)
        for flag in red_flags
    ) or any(
        any(kw in gap.lower() for kw in blocking_keywords)
        for gap in gaps
    )
    if has_blocking or score < 0.20:
        return "DECLINE"

    # 2. Strong technical + product + 3+ required skills
    if technical_score >= 0.70 and product_score >= 0.65 and len(skills_matched) >= 3:
        return "STRONGLY_ADVANCE" if score >= 0.80 else "ADVANCE"

    # 3. Moderate technical + strong product + growth
    growth_score = next(
        (v for k, v in disc.items() if "growth" in k.lower() or "ownership" in k.lower()),
        0.5,
    )
    if technical_score >= 0.50 and product_score >= 0.70 and growth_score >= 0.60:
        return "ADVANCE"

    # 4. 2+ required skills OR unverifiable pattern
    unverifiable = [f for f in red_flags if "unverif" in f.lower() or "cannot verify" in f.lower()]
    if len(skills_matched) >= 2 or unverifiable:
        return "REVIEW_FURTHER"

    # 5. Default
    return "ADVANCE_IF_POOL_THIN"


